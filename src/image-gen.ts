/**
 * Decide if Meera should send a spontaneous selfie based on the conversation
 * context, comfort tier, and mood. Like a real girl would.
 */
export function shouldSendSelfie(
  tier: string,
  mood: string,
  userText: string,
  recentHistory?: { role: string; content: string }[],
  hasAnyKey: boolean = !!STABILITY_API_KEY,
): { shouldSend: boolean; reason: "asked" | "vibe" | "spontaneous" } {
  if (!hasAnyKey) return { shouldSend: false, reason: "spontaneous" };

  const text = userText.toLowerCase();

  // ── Explicit requests (always honor for comfortable+)
  const selfieRequestPatterns = [
    /\b(send|show|share).{0,10}(pic|photo|selfie|image|face|yourself|apni photo)/i,
    /\b(selfie|photo|pic).{0,10}(bhej|pathao|de na|dena|dikha|dikhao|send|share|show)/i,
    /\b(tumi|tera|tumhar|apni|apnar).{0,10}(photo|pic|selfie|face|picture)/i,
    /\b(how do you look|kemon dekht[eai]|kaisi dikhti|kaisa dikhta)\b/i,
    /\b(face reveal|show face|dekha to|chehra dikha)/i,
    /\b(send me a pic|pic pls|photo pls|selfie pls)/i,
    /\b(pic de|photo de|selfie de|picture de|photo dao|pic dao)\b/i,
    /\b(apni chhobi|nij er photo|nijer pic)\b/i,
  ];

  if (selfieRequestPatterns.some((p) => p.test(text))) {
    // Debug mode: bypass tier check, always send when asked
    if (FORCE_SELFIE_DEBUG) {
      console.log("[ImageGen][DEBUG] Force selfie mode — bypassing tier check");
      return { shouldSend: true, reason: "asked" };
    }
    if (tier === "stranger") return { shouldSend: false, reason: "asked" }; // Too early
    return { shouldSend: true, reason: "asked" };
  }

  // ── Only comfortable+ tiers get spontaneous selfies
  if (tier !== "comfortable" && tier !== "close") {
    return { shouldSend: false, reason: "spontaneous" };
  }

  // ── Vibe-based triggers (conversation is going well)
  const vibePatterns = [
    /\b(you're cute|you're pretty|you're beautiful|sundar|khub sundar|tumi khub)\b/i,
    /\b(what are you doing|ki korcho|kya kar rahi|kya kr rahi|kya kar raha)\b/i,
    /\b(miss you|miss kori|miss korchi|miss krliya|yaad aa rahi)\b/i,
    /\b(good morning|good night|gm|gn)\b/i,
    /\b(getting ready|ready hoye|taiyar|ready ho)\b/i,
  ];

  if (vibePatterns.some((p) => p.test(text))) {
    // 15% chance on vibe trigger for comfortable, 25% for close
    const chance = tier === "close" ? 0.25 : 0.15;
    if (Math.random() < chance) {
      return { shouldSend: true, reason: "vibe" };
    }
  }

  // ── Spontaneous selfie (very rare, like a real girl randomly sending a pic)
  // Only for close tier, 3% chance per message
  if (tier === "close" && Math.random() < 0.03) {
    return { shouldSend: true, reason: "spontaneous" };
  }

  return { shouldSend: false, reason: "spontaneous" };
}
