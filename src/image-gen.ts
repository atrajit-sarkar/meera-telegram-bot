// ── Generate a per-user seed (deterministic from userId) ────────────

/**
 * Generate a stable seed for a user. This seed stays the same forever
 * so all their generated images have the same base face.
 */
export function generateUserSeed(userId: number): number {
  // Create a deterministic but well-distributed seed from userId
  // Using a simple hash that spreads across the full seed range
  let hash = userId;
  hash = ((hash >> 16) ^ hash) * 0x45d9f3b;
  hash = ((hash >> 16) ^ hash) * 0x45d9f3b;
  hash = (hash >> 16) ^ hash;
  // Keep in Stability's range [1, 4294967294]
  return (Math.abs(hash) % 4294967293) + 1;
}

