/**
 * Parse gender from customPersona text.
 * Looks for GENDER: field or obvious keywords.
 */
export function parseGenderFromPersona(persona?: string): "girl" | "boy" {
  if (!persona) return "girl"; // Default Meera is a girl
  const lower = persona.toLowerCase();

  // Check GENDER: field
  const genderMatch = lower.match(/gender\s*:\s*["']?\s*(girl|boy|male|female|man|woman|non.?binary|nb)/);
  if (genderMatch) {
    const g = genderMatch[1];
    if (g === "boy" || g === "male" || g === "man") return "boy";
    return "girl";
  }

  // Check for masculine keywords in persona
  if (/\b(he\/him|boyfriend|brother|bhai|bro type|male persona|ladka)\b/i.test(lower)) return "boy";

  return "girl";
}

/**
 * Extract appearance hints from custom persona text.
 * Looks for hair color, eye color, skin tone, style details, etc.
 */
function extractAppearanceHints(persona?: string): string {
  if (!persona) return "";
  const hints: string[] = [];
  const lower = persona.toLowerCase();

  // Hair
  const hairMatch = lower.match(/(blonde|black|red|blue|pink|purple|white|silver|short|long|curly|straight|wavy)\s+hair/g);
  if (hairMatch) hints.push(...hairMatch);

  // Eyes
  const eyeMatch = lower.match(/(blue|green|brown|hazel|amber|dark|light)\s+eyes/g);
  if (eyeMatch) hints.push(...eyeMatch);

  // Skin
  const skinMatch = lower.match(/(fair|pale|dark|light|brown|tan|olive)\s+skin/g);
  if (skinMatch) hints.push(...skinMatch);

  // Style/vibe words
  if (/\b(goth|gothic|punk|emo)\b/.test(lower)) hints.push("gothic style");
  if (/\b(kawaii|cute|soft girl)\b/.test(lower)) hints.push("soft kawaii aesthetic");
  if (/\b(tomboy)\b/.test(lower)) hints.push("tomboy style");
  if (/\b(hijab|modest)\b/.test(lower)) hints.push("wearing hijab");

  return hints.join(", ");
}

/**
 * Build the character description that stays constant across all generations.
 * This + the seed ensures face consistency.
 */
export function buildCharacterDescription(persona?: string): string {
  const gender = parseGenderFromPersona(persona);
  const baseAppearance = gender === "boy" ? DEFAULT_BOY_APPEARANCE : DEFAULT_GIRL_APPEARANCE;
  const customHints = extractAppearanceHints(persona);

  if (customHints) {
    // Override defaults with persona-specified appearance details
    return `${baseAppearance}, ${customHints}`;
  }
  return baseAppearance;
}

